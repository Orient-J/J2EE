package pkg;

public class Student {

	private String name;
	private String roll;
	private String email;
	
	public Student() {
		
	}
	
	public void setName(String name) {
		
		this.name = name;
	}
	
	public String getName() {
		
		return this.name;
	}
	
	public void setRoll(String roll) {
		
		this.roll = roll;
	}
	
	public String getRoll() {
		
		return this.roll;
	}
	
	public void setEmail(String email) {
		
		this.email = email;
	}
	
	public String getEmail() {
		
		return this.email;
	}
	
//	Remark :: you must include both set and get methods for data input and output
	
}
